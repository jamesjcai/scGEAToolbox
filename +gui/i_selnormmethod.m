function [methodid, dim] = i_selnormmethod

methodid = []; 
dim =[];
if nargout > 1
    answer = questdlg('Specify the dimension to operate on:', ...
        '', 'Column-wise (1)', 'Row-wise (2)', 'Row-wise (2)');
    switch answer
        case 'Column-wise (1)'
            dim = 1;
        case 'Row-wise (2)'
            dim = 2;
        otherwise
            return;
    end
end

listitems = {'zscore std', ...
    'zscore robust', ...
    'norm 2', ...
    'norm Inf', ...
    'scale std', ...
    'scale mad', ...
    'scale first', ...
    'scale iqr', ...
    'range [0 1]', ...
    'range [1 10]', ...    
    'center mean', ...
    'center median', ...
    'center scale', ...
    'medianiqr'};
[indx, tf] = listdlg('PromptString', {'Select Method'}, ...
    'SelectionMode', 'single', ...
    'ListString', listitems, 'ListSize', [220, 300], ...
    'InitialValue', methodid);
if tf == 1
    methodid = indx;
end

end